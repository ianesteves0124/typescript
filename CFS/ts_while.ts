// WHILE LOOP STATEMENT
//The while statement allows you to create a loop that executes a block of code as long as a condition is true.

// while(condition) {
//     // do something
// }

let counter = 0;

while (counter < 5) {
    console.log(counter);
    counter++;
}

//DO WHILE STATEMENTS
let i = 0;

do {
    console.log(i);
    i++
} while (i < 10);

